// Canonical JSON Serialization
// ============================================================================

describe('canonicalStringify', () => {
  test('sorts keys at top level', () => {
    expect(canonicalStringify({ b: 1, a: 2 })).toBe('{"a":2,"b":1}');
  });

  test('sorts keys recursively', () => {
    expect(canonicalStringify({ b: { d: 1, c: 2 }, a: 3 }))
      .toBe('{"a":3,"b":{"c":2,"d":1}}');
  });

  test('handles arrays (no key sorting)', () => {
    expect(canonicalStringify([3, 1, 2])).toBe('[3,1,2]');
  });

  test('handles null', () => {
    expect(canonicalStringify(null)).toBe('null');
  });

  test('handles strings with escapes', () => {
    expect(canonicalStringify('hello "world"')).toBe('"hello \\"world\\""');
  });

  test('handles nested arrays in objects', () => {
    expect(canonicalStringify({ b: [1, 2], a: 'x' }))
      .toBe('{"a":"x","b":[1,2]}');
  });

  test('handles booleans', () => {
    expect(canonicalStringify({ z: true, a: false })).toBe('{"a":false,"z":true}');
  });

  test('compact separators (no whitespace)', () => {
    const result = canonicalStringify({ a: 1 });
    expect(result).not.toContain(' ');
  });

  test('matches Python json.dumps(sort_keys=True, separators=(",",":"))', () => {
    // This is the critical cross-language test
    // Python: json.dumps({"v":1,"seq":0,"event":"test","prev_hash":"sha256:GENESIS"},sort_keys=True,separators=(",",":"))
    // Expected: {"event":"test","prev_hash":"sha256:GENESIS","seq":0,"v":1}
    const obj = { v: 1, seq: 0, event: 'test', prev_hash: 'sha256:GENESIS' };
    expect(canonicalStringify(obj))
      .toBe('{"event":"test","prev_hash":"sha256:GENESIS","seq":0,"v":1}');
  });

  test('handles undefined values in objects', () => {
    // undefined should be treated like null/missing
    expect(canonicalStringify({ a: undefined, b: 1 })).toBe('{"a":null,"b":1}');
  });

  test('escapes non-ASCII to \\uXXXX (matches Python ensure_ascii=True)', () => {
    // Python: json.dumps("café") → '"caf\\u00e9"'
    expect(canonicalStringify('café')).toBe('"caf\\u00e9"');
  });

  test('escapes emoji via surrogate pairs', () => {
    // Python: json.dumps("👍") → '"\\ud83d\\udc4d"'
    expect(canonicalStringify('👍')).toBe('"\\ud83d\\udc4d"');
  });

  test('escapes CJK characters', () => {
    // Python: json.dumps("你好") → '"\\u4f60\\u597d"'
    expect(canonicalStringify('你好')).toBe('"\\u4f60\\u597d"');
  });

  test('handles control characters', () => {
    expect(canonicalStringify('a\tb\nc')).toBe('"a\\tb\\nc"');
    expect(canonicalStringify('a\x00b')).toBe('"a\\u0000b"');
  });

  test('non-ASCII in object keys', () => {
    expect(canonicalStringify({ café: 1 })).toBe('{"caf\\u00e9":1}');
  });
});

