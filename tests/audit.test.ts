describe('AuditWriter — Legacy', () => {
  let dir: string;

  beforeEach(() => { dir = tmpDir(); });
  afterEach(() => { fs.rmSync(dir, { recursive: true, force: true }); });

  test('verify handles legacy entries (no prev_hash)', () => {
    const mp = memoryPath(dir);
    const activePath = AuditWriter._deriveActivePath(mp);

    // Write legacy-format entries (no v, seq, prev_hash)
    const legacy = [
      JSON.stringify({ timestamp: '2026-01-01T00:00:00Z', event: 'prune', nodes: [], reason: 'test' }),
      JSON.stringify({ timestamp: '2026-01-02T00:00:00Z', event: 'prune', nodes: [], reason: 'test2' }),
    ];
    fs.writeFileSync(activePath, legacy.join('\n') + '\n', 'utf-8');

    const result = AuditWriter.verify(activePath);
    expect(result.valid).toBe(true);
    expect(result.legacyEntries).toBe(2);
    expect(result.totalEntries).toBe(2);
  });

  test('new entries after legacy use LEGACY_BRIDGE sentinel', () => {
    const mp = memoryPath(dir);
    const activePath = AuditWriter._deriveActivePath(mp);

    // Write legacy entry
    fs.writeFileSync(activePath,
      JSON.stringify({ timestamp: '2026-01-01T00:00:00Z', event: 'prune', reason: 'old' }) + '\n',
      'utf-8'
    );

    // New writer appends with hash chain
    const writer = new AuditWriter(mp);
    writer.write('new_event', { x: 1 });

    const lines = fs.readFileSync(activePath, 'utf-8').trim().split('\n');
    expect(lines).toHaveLength(2);
    const newEntry = JSON.parse(lines[1]);
    expect(newEntry.prev_hash).toBe(LEGACY_BRIDGE_HASH);
  });

  test('recovery after legacy→chained transition preserves chain (not LEGACY_BRIDGE)', () => {
    const mp = memoryPath(dir);
    const activePath = AuditWriter._deriveActivePath(mp);

    // Write legacy entry (no hash chain)
    fs.writeFileSync(activePath,
      JSON.stringify({ timestamp: '2026-01-01T00:00:00Z', event: 'prune', reason: 'old' }) + '\n',
      'utf-8'
    );

    // Writer 1: transition from legacy to chained
    const writer1 = new AuditWriter(mp);
    writer1.write('e1', { x: 1 }); // LEGACY_BRIDGE
    writer1.write('e2', { x: 2 }); // chains from e1

    // Writer 2: restart (simulating process restart)
    // Should chain from e2's hash, NOT reset to LEGACY_BRIDGE
    const writer2 = new AuditWriter(mp);
    writer2.write('e3', { x: 3 });

    // Full chain should be valid
    const result = AuditWriter.verify(activePath);
    expect(result.valid).toBe(true);
    expect(result.totalEntries).toBe(4); // legacy + bridge + chained + post-restart

    // Verify the post-restart entry chains properly (not LEGACY_BRIDGE)
    const lines = fs.readFileSync(activePath, 'utf-8').trim().split('\n');
    const e3 = JSON.parse(lines[3]);
    expect(e3.prev_hash).not.toBe(LEGACY_BRIDGE_HASH);
    expect(e3.prev_hash).toBe(AuditWriter._computeHash(lines[2]));
  });

  test('verify passes mixed legacy + chained entries', () => {
    const mp = memoryPath(dir);
    const activePath = AuditWriter._deriveActivePath(mp);

    // Legacy entry
    fs.writeFileSync(activePath,
      JSON.stringify({ timestamp: '2026-01-01T00:00:00Z', event: 'prune', reason: 'old' }) + '\n',
      'utf-8'
    );

    // Chained entries
    const writer = new AuditWriter(mp);
    writer.write('e1', {});
    writer.write('e2', {});

    const result = AuditWriter.verify(activePath);
    expect(result.valid).toBe(true);
    expect(result.legacyEntries).toBe(1);
    expect(result.totalEntries).toBe(3);
  });
});

