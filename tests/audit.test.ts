describe('AuditWriter — Hash Chain', () => {
  let dir: string;

  beforeEach(() => { dir = tmpDir(); });
  afterEach(() => { fs.rmSync(dir, { recursive: true, force: true }); });

  test('first entry has GENESIS prev_hash', () => {
    const mp = memoryPath(dir);
    const writer = new AuditWriter(mp);
    writer.write('e1', {});

    const entry = JSON.parse(fs.readFileSync(writer.activePath, 'utf-8').trim());
    expect(entry.prev_hash).toBe(GENESIS_HASH);
  });

  test('second entry chains from first', () => {
    const mp = memoryPath(dir);
    const writer = new AuditWriter(mp);
    writer.write('e1', {});
    writer.write('e2', {});

    const lines = fs.readFileSync(writer.activePath, 'utf-8').trim().split('\n');
    const e1 = JSON.parse(lines[0]);
    const e2 = JSON.parse(lines[1]);

    // e2.prev_hash should be SHA256 of e1's JSON line
    const expectedHash = AuditWriter._computeHash(lines[0]);
    expect(e2.prev_hash).toBe(expectedHash);
  });

  test('verify() confirms valid chain', () => {
    const mp = memoryPath(dir);
    const writer = new AuditWriter(mp);
    writer.write('e1', { a: 1 });
    writer.write('e2', { b: 2 });
    writer.write('e3', { c: 3 });

    const result = AuditWriter.verify(writer.activePath);
    expect(result.valid).toBe(true);
    expect(result.totalEntries).toBe(3);
    expect(result.filesVerified).toBe(1);
    expect(result.legacyEntries).toBe(0);
  });

  test('verify() detects tampered entry', () => {
    const mp = memoryPath(dir);
    const writer = new AuditWriter(mp);
    writer.write('e1', { a: 1 });
    writer.write('e2', { b: 2 });
    writer.write('e3', { c: 3 });

    // Tamper with the second line
    const content = fs.readFileSync(writer.activePath, 'utf-8');
    const lines = content.split('\n');
    const e2 = JSON.parse(lines[1]);
    e2.data.b = 999;
    lines[1] = JSON.stringify(e2);
    fs.writeFileSync(writer.activePath, lines.join('\n'), 'utf-8');

    const result = AuditWriter.verify(writer.activePath);
    expect(result.valid).toBe(false);
    expect(result.chainBreakAt).toBeDefined();
  });

  test('hash chain disabled when hashChain=false', () => {
    const mp = memoryPath(dir);
    const writer = new AuditWriter(mp, { hashChain: false });
    writer.write('e1', {});

    const entry = JSON.parse(fs.readFileSync(writer.activePath, 'utf-8').trim());
    expect(entry.prev_hash).toBeUndefined();
  });

  test('deterministic serialization produces same hash across calls', () => {
    const data = { v: 1, seq: 0, timestamp: '2026-03-21T00:00:00.000Z', event: 'test', prev_hash: GENESIS_HASH, session_id: null, data: { x: 1 }, adapter: null };
    const json1 = canonicalStringify(data);
    const json2 = canonicalStringify(data);
    expect(json1).toBe(json2);
    expect(AuditWriter._computeHash(json1)).toBe(AuditWriter._computeHash(json2));
  });
});

